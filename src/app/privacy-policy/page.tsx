----------------------------------------------------------------
・当団体は、正規留学生（regular-international）に該当する利用者について、当該手続きに必要な住所等の追加情報を取得します。
・住所等は、当該手続きの目的達成に必要な最小限の期間に限り保管し、提出完了後に速やかに削除します。
・保存が法令により必要となる場合には、法令に基づく範囲でのみ保管します。

